package org.geospatialweb.geometry;

public class Point {
	double x;
	double y;
	double z;
	
	void setX(double x){
		this.x = x;
	}
	
	void setY(double y){
		this.y = y;
	}
	
	void setZ(double z){
		this.z = z;
	}
	
	double getX(){
		return x;
	}
	
	double getY(){
		return y;
	}

	double getZ(){
		return z;
	}

}
